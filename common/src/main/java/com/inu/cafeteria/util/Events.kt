package com.inu.cafeteria.util

import com.google.firebase.analytics.FirebaseAnalytics
import com.google.firebase.analytics.ktx.analytics
import com.google.firebase.analytics.ktx.logEvent
import com.google.firebase.ktx.Firebase

object Events {
    private val analytics = Firebase.analytics

    fun onLogin(userId: Int) {
        analytics.logEvent(FirebaseAnalytics.Event.LOGIN) {
            param(FirebaseAnalytics.Param.ITEM_ID, userId.toString())
        }
    }

    fun onSelectTab(tabItemName: String) {
        analytics.logEvent(FirebaseAnalytics.Event.SELECT_ITEM) {
            param(FirebaseAnalytics.Param.ITEM_ID, tabItemName)
        }
    }

    fun onUserDiscoveredEasterEgg(easterEggName: String) {
        analytics.logEvent("discover_easter_egg") {
            param("easter_egg_name", easterEggName)
        }
    }
}