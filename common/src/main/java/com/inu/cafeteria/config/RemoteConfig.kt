package com.inu.cafeteria.config

import com.google.firebase.ktx.Firebase
import com.google.firebase.remoteconfig.ktx.remoteConfig
import com.google.firebase.remoteconfig.ktx.remoteConfigSettings
import com.inu.cafeteria.common.R

object RemoteConfig {
    private val remoteConfig = Firebase.remoteConfig.apply {
        setConfigSettingsAsync(remoteConfigSettings {
            minimumFetchIntervalInSeconds = 3600
        })

        setDefaultsAsync(R.xml.config_defaults)

        safeFetch()
    }

    private fun safeFetch() {
        try {
            remoteConfig.fetchAndActivate()
        } catch (e: Exception) {
            e.printStackTrace()
        }
    }

    fun getBoolean(key: String) = remoteConfig.getBoolean(key)
    fun getString(key: String) = remoteConfig.getString(key)
}